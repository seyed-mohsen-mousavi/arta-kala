import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "@/styles/globals.css";
import Navbar from "@/components/Navbar";
import { GetShopCategoriesTreeList } from "@/services/shopActions";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "تکنو صاف",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const result = await GetShopCategoriesTreeList();

  return (
    <html lang="fa-IR" dir="rtl" className="scroll-smooth w-screen">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased overflow-x-hidden overflow-y-auto w-full h-full`}
      >
        <Navbar categories={result?.data} />
        <main className="container customSm:max-w-[566px]  w-full mx-auto mb-20 px-2 lg:px-0">
          {children}
        </main>
      </body>
    </html>
  );
}
